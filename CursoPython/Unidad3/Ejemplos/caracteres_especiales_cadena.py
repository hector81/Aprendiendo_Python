#Los caracteres especiales empiezan por una contrabarra (\).
#Comilla doble: \"
print("Las comillas dobles \" en cadenas escritas con comillas dobles.")
#Las comillas dobles " en cadenas escritas con comillas dobles.

#Comilla simple: \'
print('Las comillas simples \' en cadenas escritas con comillas simples.')
#Las comillas simples ' en cadenas escritas con comillas simples.

#Salto de línea: \n
print("Una línea\nOtra línea")
#Una línea
#Otra línea

#Tabulador: \t
print("1\t2\t3")
#1     2     3