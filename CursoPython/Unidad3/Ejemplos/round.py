# La función round() redondea un número flotante a una precisión dada en dígitos decimales. 
# Esto siempre devuelve un número flotante.

# En el siguiente ejemplo verás el redondeo de un número flotante a entero, mayor o igual a .5 al alza:
print("round(7.5) ==> " , round(7.5)) # 8.0

# En este otro ejemplo, se hará el redondeo de un número flotante a entero, menor de .5 a la baja:
print("round(4.4) ==> " , round(4.4)) # 4.0
