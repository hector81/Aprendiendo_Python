# Para unir dos textos, basta con usar el operador +.
cadena1="Hola" + "Caracola"
print(cadena1)

# Como las dos cadenas están escritas sin espacios, la concatenación también las une sin espacio. Para solucionarlo, tienes dos opciones: o bien añades un espacio al final de la primera cadena o al principio de la segunda, o bien añades una cadena que sea únicamente un espacio en blanco.
cadena2="Hola " + "Caracola"
print(cadena2)


cadena3="Hola" + " " + "Caracola"
print(cadena3)