# La función int() devuelve un número entero. 
print("int(2.5) ==> " , int(2.5))
# También puede convertir una cadena de caracteres a un número entero.
print('int("23") ==> ' , int("23"))
# La función int() sólo procesa correctamente cadenas que contengan exclusivamente números. Error.
print('int("2.5") ==> ' , int("2.5"))
print('int("doscientos") ==> ' , int("doscientos"))
