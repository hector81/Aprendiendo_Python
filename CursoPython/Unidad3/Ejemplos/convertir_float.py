'''
La función float() devuelve un número coma flotante float. Es un constructor que crea un
 coma flotante a partir de un entero o una cadena de caracteres que sean coherentes 
 con un número entero.
'''
print("float(2) ==> " , float(2)) # 2.0
print("float(2.5) ==> " , float(2.5)) # 2.5
print('float("2") ==> ' , float("2")) # 2.0
print('float("2.5") ==> ' , float("2.5")) # 2.5
